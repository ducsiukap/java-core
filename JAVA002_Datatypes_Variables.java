// Datatypes in java
/*
 * Primitives: 
 * boolean
 * char
 * byte, short, int, long
 * float, double
 */
/*
 * Non-primitives: -> Objects types, Reference types
 */

public class JAVA002_Datatypes_Variables {
    public static void main(String[] args) {
        // Syntax to declare a variable
        // datatype variableName = value;
        boolean boolVar = false;
        System.out.println("Boolean Variable: " + boolVar);
        int intVar = 13;
        System.out.println("Integer Variable: " + intVar);
        float floatVar = 3.14f;
        System.out.println("Float Variable: " + floatVar);
        String strVar = "vduczz";
        System.out.println("String Variable: " + strVar);

        System.out.println("\n--------------------");
        System.out.println("#vduczz");
    }

}
